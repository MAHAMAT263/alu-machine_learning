'''
This function def np_transpose that transposes matrix
assuming that matrix can be interpreted as a numpy.ndarray
it is not allowed to use any loops or conditional statements
it must return a new numpy.ndarray
'''


def np_transpose(matrix):
    '''
    This returns a transposition of a matrix
    using numpy. You can alos use the np.transpose
    '''
    return matrix.T
